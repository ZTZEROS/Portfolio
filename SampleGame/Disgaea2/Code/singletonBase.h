#pragma once
template <typename T>
class singletonBase
{
protected:
	//싱글톤 인스턴스 선언
	static T* singleton;

	singletonBase(void){};
	~singletonBase(void){};

public:
	static T* getsingleton(void);
	void releaseSingleton(void);
};

//싱글톤 초기화
template <typename T>
T* singletonBase<T>::singleton = 0;

//싱글톤 값 가져온다
template <typename T>
T* singletonBase<T>::getsingleton(void)
{
	if (!singleton) singleton = new T;

	return singleton;
}

//싱글톤 메모리 해제
template<typename T>
void singletonBase<T>::releaseSingleton(void)
{
	if (singleton)
	{
		delete singleton;

		singleton = 0;
	}
}